sprites.onOverlap(SpriteKind.Player, SpriteKind.Projectile, function (sprite, otherSprite) {
    if (info.countdown() == 0) {
        info.changeLifeBy(-2)
        sprites.destroy(otherSprite)
    } else {
        info.changeLifeBy(-1)
        sprites.destroy(otherSprite)
    }
})
info.onLifeZero(function () {
    game.gameOver(false)
})
